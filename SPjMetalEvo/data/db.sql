CREATE TABLE IF NOT EXISTS instance ( 
	ID INTEGER PRIMARY KEY NOT NULL,
	ALGORITHM_NAME VARCHAR(100) NOT NULL,
	MU INT,
	LAMBDA INT,
	POPSIZE INT,
	MUTATION VARCHAR(100),
	REBUILD_TYPE VARCHAR(100),
	CROSSOVER VARCHAR(100),
	SELECTION VARCHAR(100),
	CENTERING VARCHAR(100) NOT NULL,
	PARTITION VARCHAR(100) NOT NULL,
	PATHBUILDER VARCHAR(100) NOT NULL,
	GRAPH_NAME VARCHAR(40) NOT NULL,
	NUM_AGENTS INT NOT NULL,
	NUM_TURNS_ON_MEASURE INT NOT NULL,
	MAX_NUM_EVALUATIONS INT NOT NULL,
	METRIC VARCHAR(100) NOT NULL,
	ELAPSED_TIME INT NOT NULL,
	BEST_METRIC_VALUE REAL NOT NULL,
	BEST_STRING TEXT NOT NULL,
	BEST_FILE VARCHAR(40),
	EVAL_NUMBER_OF_BEST INT NOT NULL
);